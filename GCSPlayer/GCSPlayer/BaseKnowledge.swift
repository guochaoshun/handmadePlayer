 1. 为什么编码压缩必须用 YUV，而坚决不用 RGB？
    - 【数学去相关性】：RGB 三个通道的数据在自然界中是"高度相关"的（比如一面灰墙，R=G=B=128）。直接压缩 RGB 会导致极大的信息冗余。
      YUV 的转换本质上是"去相关性"的数学魔法，它将 90% 的能量集中在 Y（亮度）通道，使得 U、V（色差）通道能量极低，存在大量接近 0 的数据。
      这种能量集中的特性，极其适合后续的 DCT 变换和哈夫曼编码进行极限压缩。
    - 【生理学压缩】：人眼的视网膜对"亮度(Y)"极其敏感，但对"色彩细节(U,V)"相对迟钝。
      科学家利用这个生理漏洞，发明了色度子采样（如 YUV420）：保留 100% 的亮度(Y)，但每 4 个像素只共用 1 个 U 和 1 个 V。
      结果：在还没开始真正的算法压缩前，仅仅通过丢弃部分 UV，数据量就直接被物理砍半，且人眼几乎无法察觉。
      
 2. 为什么显示器硬件只认 RGB，不能直接造一个 YUV 屏幕？
    - 【物理发光限制】：屏幕（LCD/OLED）是发光设备。光学上的三原色就是红、绿、蓝，通过"加法混色"合成自然界所有颜色。
      YUV 是纯数学构造，U 和 V 是"色差"（相对值，且有负数），物理世界根本造不出能发射"蓝色差光"或"负的光"的灯管。
    - 【生物学解剖限制】：人类的视网膜上有三种视锥细胞，分别对长波(偏红)、中波(偏绿)、短波(偏蓝)最敏感。
      屏幕只需要点亮红绿蓝三个子像素，就能完美欺骗大脑。

 3. 视频滤镜与美颜的底层实现原理
    - 视频/直播中的美颜和滤镜功能，本质上是在自定义渲染管线（如 Metal/OpenGL 的 Shader）中实现的。
    - 核心逻辑：在 YUV 转 RGB 的计算过程中（或转换后），利用 GPU 千万级的并发计算能力对像素值进行数学变换。
    - 比如：调整对比度/饱和度实现滤镜；使用高斯模糊加边缘检测实现磨皮。
    - 这也是为什么大厂播放器通常不使用系统封闭的 AVSampleBufferDisplayLayer，而是必须手写 Metal/OpenGL 渲染管线的核心商业原因。

 4. FFmpeg 自定义 I/O 上下文 (AVIOContext) 的本质
    - `avio_alloc_context` 用于创建一个自定义的 I/O 上下文。
    - 核心作用：将底层的数据读取（read）、写入（write）和寻址（seek）操作的控制权，从 FFmpeg 原生的文件系统接口接管到开发者手写的 C 回调函数中。
    - 传递给回调函数的 `opaque` 指针所指向的结构体是完全自定义的黑盒。唯一必须的字段是底层文件描述符 `fd` 等状态数据，其余如解密密钥、脏数据长度等均是业务附加信息。
    
 5. 基于 AVIOContext 实现网络播放器的正确架构
    - 在实现网络播放器（边下边播）时，绝对不能在 `read_packet` 回调中直接发起同步网络请求，否则会严重阻塞 FFmpeg 解析线程，导致播放器卡死。
    - 工业级做法：设计一个独立的环形缓冲区（Ring Buffer），开启专门的下载线程异步拉取数据填入缓冲区；`read_packet` 只负责极速从内存缓冲区中读取数据（无数据则短暂挂起或返回特定错误码），实现 I/O 与解码的解耦。

 6. FFmpeg 必须调用 avformat_find_stream_info 的原因
    - `avformat_open_input` 只能读取文件的容器头部信息（看封面）。
    - 必须随后调用 `avformat_find_stream_info`。该函数会尝试读取并解码文件开头的一小段实际音视频数据（试读正文），从而探测出极其关键的内部信息：
      视频的真实宽高、像素格式、帧率（FPS）、时间基（Time Base），以及解码极其依赖的 `extradata`（如 H.264 的 SPS/PPS 参数集）。
    - 如果不调用，解码器将无法成功打开或音视频无法同步。

 7. FFmpeg 流遍历与解码器上下文 (AVCodecContext)
    - 多媒体文件（如 MP4）是一个包含多条“流（AVStream）”的容器（多车道高速公路）。
    - 初始化阶段必须遍历 `nb_streams`，找到需要的视频流和音频流的索引。
    - 随后通过流的 `codecpar` 找到对应的解码器（AVCodec），并创建一个解码器上下文（AVCodecContext）实例。
    - `AVCodecContext` 是 FFmpeg 解码引擎的“工作台”，保存了解码该流所需的所有状态（宽高、采样率等）。

 8. 播放器音频处理的三段式核心链路
    - ① 解码（Decoding）：使用 `AVCodecContext` 将压缩的音频包（如 AAC）解码为原始的 PCM 数据（此时可能是 48kHz, Float32 平面格式）。
    - ② 重采样（Resampling）：由于解码出的 PCM 格式往往与底层硬件要求不匹配，必须使用 `SwrContext` 将其翻译转换为硬件指定的格式（如 44.1kHz, S16 交错格式）。
    - ③ 硬件播放（Playback）：通过 iOS 的 `AudioUnit` 接口，以“拉取（Pull）”模式高频回调，从重采样缓冲区中获取数据并驱动扬声器发声。
    
 9. FFmpeg AVIOContext Seek 回调的 whence 模式解析
    - `SEEK_SET`: 绝对定位。把指针移动到距离"逻辑文件开头" offset 字节处。
    - `SEEK_CUR`: 相对定位。把指针移动到距离"当前位置" offset 字节处。
    - `SEEK_END`: 尾部定位。把指针移动到距离"文件末尾" offset 字节处（offset 通常为负数）。
    - `AVSEEK_SIZE`: (FFmpeg 魔改模式) 不移动指针，仅仅是询问"这个逻辑文件总共有多大"。开发者必须返回剔除脏数据等额外开销后的纯净业务数据大小。

 10. 底层文件读取（read）后偏移量回溯的正确计算公式
    - 在底层文件读取（如 `read()`）后回溯计算起始偏移量时，不能简单使用 `当前偏移量 - 期望读取的缓冲区大小(buf_size)`。
    - 因为 `read()` 的实际返回字节数（`bytesRead`）可能小于 `buf_size`（例如遇到文件末尾 EOF 时）。必须使用 `当前偏移量 - 实际读取的字节数(bytesRead)` 才能准确无误地倒推回本次读取的起始物理位置。

 11. 获取 read 系统调用起始偏移量的正确时序
    - 在使用 `read()` 等系统调用读取文件后，底层文件描述符（fd）的物理指针会自动向后移动 `bytesRead` 个字节。
    - 因此，如果在 `read()` 执行之后再去通过 `lseek(fd, 0, SEEK_CUR)` 获取当前物理偏移量，拿到的是读取后的新位置，而非起始位置。
    - 必须在 `read()` 调用之前获取当前的物理偏移量，否则会导致如边界判断等逻辑错误。

 12. 大文件局部修改的性能测试与 I/O 原则
    - 在验证大文件局部修改（如加解密头部、追加脏数据）的性能时：
      1. 必须使用 `FileHandle(forUpdating:)` 进行真实的就地操作（覆盖或追加），避免全量拷贝生成新文件的 I/O 耗时掩盖算法耗时。
      2. 若需隔离测试环境，应提前利用 APFS 的 clone 特性极速拷贝文件后再进行就地修改。
      3. 跨平台底层不支持在文件头部真实“插入”数据，iOS 上只能通过全量 I/O 重写或尾部追加来模拟。

